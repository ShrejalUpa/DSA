//Assignment Operator

public class p19 {
    public static void main(String args[]){
        int a = 100 , b = 200 , c = 300 , d = 200;
        System.out.println(a+=b);
        System.out.println(a=d);
        System.out.println(d-=b);
        System.out.println(a*=c);
        System.out.println(a%=c);
    }
}
