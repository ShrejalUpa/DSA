//UNARY OPERATOR

public class p20 {
    public static void main(String[] args){
        int p = 5 , q = 9;
        System.out.println(p++);
        System.out.println(p--);
        System.out.println(++p);
        System.out.println(--p);
        int x = p++;
        int y = ++q;
        System.out.println(x);
        System.out.println(y);
    }
}
